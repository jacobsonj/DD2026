//arrays store multiple values in a single variable using square brackets []
let fruits = ["Apple", "Banana", "Cherry", "Date"]; 
console.log(fruits[0]); // Accessing first element: Apple
console.log(fruits.length); // Number of elements: 4
fruits.push("Elderberry"); // Adding a new element
console.log(fruits); // Updated array: ["Apple", "Banana", "Cherry", "Date", "Elderberry"]
fruits.pop(); // Removing the last element
console.log(fruits); // Updated array: ["Apple", "Banana", "Cherry", "Date"]
